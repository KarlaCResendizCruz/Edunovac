      <h3>Profesionalizacíon y Capacitación</h3>
      <div class="col-md-12">
      <p>La profesionalización docente se ha convertido hoy en día en un gran reto para el magisterio, dado que engloba dela la formación inicial de los docentes, por parte de las instituciones encargadas para ello, así como la formación continua o permanente de los maestros en servicio. La profesionalización docente es entendida desde el punto de vista de los docentes como un gran reto a lograr, el camino a seguir para una carrera exitosa donde los frutos se vean reflejados en el momento en que se encuentren en las aulas.</p> 
      <p>Al hablar de profesionalización docente se debe entender primero que es un profesional, lo cual entendemos que es una persona dotada de habilidades específicas, especializadas, todas adquiridas de la ciencia o bien; de conocimientos surgidos de la práctica docente. El profesional es una persona capaz de hacer valer todo lo antes mencionado para saber resolver cualquier situación. En esto podemos plantear la siguiente interrogante ¿Alguien puede decir que ha logrado el máximo nivel de profesionalización?</p>
      <p>Se define a la profesionalización docente como el proceso mediante el cual se mejoran las habilidades del docente para lograr un nivel de competitividad, donde están involucradas sus habilidades, capacidades y destrezas desde su formación inicial, hasta su desarrollo en el aula y en relación a los resultados obtenidos en el aprendizaje de sus alumnos, transformando su profesión y logrando la más alta integridad y competencia. (Santibáñez, 2016).</p>
      <p>Este manual sobre la profesionalización docente, contiene diversos elementos que pueden aplicarse con el propósito de mejorar la práctica docente y coadyuvar en los procesos de enseñanza- aprendizaje de la educación física de los estudiantes.</p> 
